import time


def get_sorted_pair(i, j, k):
    f = min(i, min(j, k))
    t = max(i, max(j, k))
    s = -(f + t)
    return [f, s, t]


def three_sum(nums):
    if len(nums) <= 2:
        return []

    nums.sort()

    pairs = {}
    freq = {}
    iter_i = {}

    for i in range(len(nums)):
        if nums[i] not in freq:
            iter_i[nums[i]] = i
        freq[nums[i]] = i

    for key, index in iter_i.items():
        ni = key
        i = index
        for j in range(i, len(nums)):
            nj = nums[j]
            if i != j:
                nk = ni + nj
                k = i

                if nk == 0 and nk in freq:
                    k = freq[nk]
                else:
                    nk = -nk
                    if nk in freq:
                        k = freq[nk]

                if (i != k and j != k) and (ni + nj + nk == 0):
                    pair = get_sorted_pair(ni, nj, nk)
                    pair_key = f"{pair[0]}{pair[1]}{pair[2]}"
                    if pair_key not in pairs:
                        pairs[pair_key] = pair

    return list(pairs.values())


